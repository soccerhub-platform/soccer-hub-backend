package kz.edu.soccerhub.client.domain.enums;

import lombok.Getter;

@Getter
public enum ClientStatus {

    /** Клиент только создан диспетчером, заявка ещё не обработана */
    NEW("Новый"),

    /** Админ или менеджер работает с лидом (обзвон, уточнение данных) */
    IN_PROGRESS("В работе"),

    /** Не удалось связаться — клиент не отвечает на звонки / сообщения */
    NO_RESPONSE("Нет связи"),

    /** Клиент отказался или не заинтересован */
    REJECTED("Отказ"),

    /** Назначен пробный урок, клиент подтвердил участие */
    TRIAL_SCHEDULED("Назначен пробный"),

    /** Пробный урок состоялся, ожидается решение */
    TRIAL_COMPLETED("Пробный проведён"),

    /** После пробного клиент не записался / не подходит */
    TRIAL_FAILED("Пробный неудачный"),

    /** Клиент готов оформить договор, но ещё не завершил оплату */
    CONTRACT_PENDING("Оформление договора"),

    /** Клиент оплатил и посещает тренировки */
    ACTIVE("Активный"),

    /** Временно не посещает (каникулы, травма и т.п.) */
    PAUSED("Приостановлен"),

    /** Клиент закончил обучение или не активен длительное время */
    INACTIVE("Неактивный");

    private final String label;

    ClientStatus(String label) {
        this.label = label;
    }
}